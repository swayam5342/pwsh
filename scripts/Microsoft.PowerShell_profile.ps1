Function quote {
    param (
    [string]$Type = "random"
    )
    $baseUrl = "https://zenquotes.io/api/"
    $url = if ($Type -eq "today" -or $Type -eq "author") {
        $baseUrl + $Type
    } else {
        $baseUrl + "random"
    }
    $response = Invoke-RestMethod -Uri $url -Method Get
    if ($Type -eq "author") {
        $quote = $response[0].q
        $author = $response[0].a
        Write-Output "Quote by ${author}: $quote"
    } else {
        $quote = $response[0].q
        Write-Output "Quote: $quote"
    }
}


